package people.dict;

public class FirstName {

	private String name;
	
	boolean sex;

	public final String getName() {
		return name;
	}

	public final void setName(String name) {
		this.name = name;
	}

	public final boolean isSex() {
		return sex;
	}

	public final void setSex(boolean sex) {
		this.sex = sex;
	}
	
	
}
